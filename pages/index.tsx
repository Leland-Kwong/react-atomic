import { useEffect } from 'react'
import type { NextPage } from 'next'
import Head from 'next/head'
import styles from '../styles/Home.module.css'
import {
  atom,
  useAtom,
  useSetAtom,
  useResetAtom,
  AtomRoot
} from '../libs/atomic-state'

interface Hello {
  text: string
}

const helloRef = atom<Hello>({
  key: 'Hello',
  defaultState: { text: 'Hello world' }
})

type TimeElapsed = number

const timerRef = atom<TimeElapsed>({
  key: 'TimeElapsed',
  defaultState: 0
})

const tick = (time: TimeElapsed, incrementBy: number) =>
  time + incrementBy

const SubComponent = () => {
  const count = useAtom(timerRef, (s) => s)
  const update = useSetAtom(timerRef)
  const reset = useResetAtom(timerRef)

  useEffect(() => {
    const timer = setTimeout(() => {
      update(tick, 1)
    }, 1000)

    return () => clearTimeout(timer)
  }, [update, count])
  useEffect(() => reset, [reset])

  return <div>Time Elapsed: {count}s</div>
}

const setText = (_: Hello, text: string) => ({ text })

const AtomicStateDemo = () => {
  const text = useAtom(helloRef, (s) => s.text)
  const update = useSetAtom(helloRef)
  const showSubComponent = text.length > 0

  return (
    <div>
      <input
        type="text"
        value={text}
        onChange={(ev) => {
          update(setText, ev.target.value)
        }}
      />
      {showSubComponent && <SubComponent />}
    </div>
  )
}

const Home: NextPage = () => {
  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta
          name="description"
          content="Generated by create next app"
        />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <h1>Atomic State</h1>

        <AtomRoot>
          <AtomicStateDemo />
        </AtomRoot>
      </main>
    </div>
  )
}

export default Home
